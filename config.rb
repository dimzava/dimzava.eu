helpers do
    def nav_link_to(link, url, opts={})
      if active(url)
        prefix = '<li role="presentation" class="active">'
      else
        prefix = '<li role="presentation">'
      end
      prefix + link_to(link, url, opts) + "</li>"
    end
    
    def active(url)
      result = false
      if (url == '/' && current_resource.url == url) || ( url != '/' && (current_resource.url.include? url) )
        result = true
      end
      result
    end
end


